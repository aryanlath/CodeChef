#include<bits/stdc++.h>
using namespace std;
int main()
{
    int T;
    cin>>T;
    while (T--)
    {
        int c;
        int l;
        cin>>l;
        while (l--)
        {
            char ch;
            cin>>ch;
            if(ch=='M'|| ch=='m')
            {
                c=1;
            }
        }
        if(c==1)
        {
            cout<<"YES"<<endl;
        }
        else
        {
            cout<<"NO"<<endl;
        }
        c=0;
    }
}